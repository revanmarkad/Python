place = "madhi"

print(len(place))  # 5

print(place[: len(place) - 3])  # ma

print(
    place[-3:-1]
)  # len of place -3 =2 // len of place -1 =4 //  conclusion is 2:4  then start from second index and go to third index thats why ans is 'dh'


